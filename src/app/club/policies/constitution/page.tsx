import type { Metadata } from "next";
import { PolicyPage } from "@/components/ui/PolicyPage";

export const metadata: Metadata = {
  title: "Club Rules / Constitution",
  description:
    "Feering Falcons Youth Football Club rules and constitution - the binding agreement between all members of the club.",
};

export default function ConstitutionPage() {
  return (
    <PolicyPage title="Club Rules / Constitution">
      <p className="text-sm text-falcon-charcoal/40">Version Sept 2023</p>

      <h2>1. Name</h2>
      <p>
        The club shall be called Feering Falcons Youth Football Club (the
        &ldquo;Club&rdquo;) and shall be affiliated to the Essex County Football
        Association.
      </p>

      <h2>2. Objects</h2>
      <p>
        The objects of the Club shall be to provide facilities, promote the game
        of Association Football, to arrange matches and social activities for its
        members and community participation.
      </p>

      <h2>3. Status of Rules</h2>
      <p>
        These rules (the &ldquo;Club Rules&rdquo;) form a binding agreement
        between each member of the Club.
      </p>

      <h2>4. Rules and Regulations</h2>
      <ol type="a">
        <li>
          The members of the Club shall so exercise their rights, powers and
          duties and shall, where appropriate use their best endeavours to ensure
          that others conduct themselves so that the business and affairs of the
          Club are carried out in accordance with the Rules and Regulations of
          The Football Association Limited (&ldquo;The FA&rdquo;), County
          Football Association to which the Club is affiliated (&ldquo;Parent
          County Association&rdquo;) and Competitions in which the Club
          participates, for the time being in force.
        </li>
        <li>
          No alteration to the Club Rules shall be effective without prior
          written approval by the Parent County Association. The FA and the
          Parent County Association reserve the right to approve any proposed
          changes to the Club Rules.
        </li>
        <li>
          The Club will also abide by The FA&apos;s Child Protection Policies
          and Procedures, Codes of Conduct and the Equal Opportunities and
          Anti-Discrimination Policy as shall be in place from time to time.
        </li>
      </ol>

      <h2>5. Club Membership</h2>
      <ol type="a">
        <li>
          Membership shall consist Playing Members, Parents of Playing Members
          and Club Committee Members who from time to time shall be those persons
          listed in the register of members (the &ldquo;Membership
          Register&rdquo;) which shall be maintained by the Club Secretary.
        </li>
        <li>
          Membership of the Club shall be open to anyone interested in the sport
          on application, regardless of sex, age, disability, ethnicity,
          nationality, sexual orientation, religion or other beliefs, except as a
          necessary consequence of the requirement to participate in youth
          football.
        </li>
        <li>
          The Members of the Club may refuse membership, or remove it, only for
          good cause such as conduct or character likely to bring the Club or
          sport into disrepute.
        </li>
        <li>
          Appeal against refusal or removal may be made to the members in
          accordance with the Complaints Procedure in force.
        </li>
        <li>
          Membership shall become effective upon an applicant&apos;s name being
          entered in the Membership Register.
        </li>
        <li>
          The FA and Parent County Association shall be given access to the
          Membership Register on demand.
        </li>
      </ol>

      <h2>6. Annual Membership Fee</h2>
      <ol type="a">
        <li>
          An annual fee payable by each member shall be determined from time to
          time by the Club Committee. Any fee shall be payable in full upon a
          successful application for membership and annually by each member.
          Applications without the correct membership fees will not be accepted.
          Fees shall not be repayable.
        </li>
        <li>
          The Club Committee shall have the authority to levy further
          subscriptions from the members as are reasonably necessary to fulfil
          the objects of the Club.
        </li>
        <li>
          The Club may have different classes of membership and subscription on a
          non-discriminatory and fair basis. The Club will keep subscriptions at
          levels that will not pose a significant obstacle to people
          participating.
        </li>
      </ol>

      <h2>7. Resignation and Expulsion</h2>
      <ol type="a">
        <li>
          A member shall cease to be a member of the Club if, and from the date
          on which, he/she gives written notice to the Club Secretary of his/her
          resignation. Any such notice is to be accompanied by payment of all
          outstanding monies due to the Club. A member whose annual membership
          fee or further subscription is more than two (2) months in arrears
          shall be deemed to have resigned unless agreed otherwise by the Club
          Committee.
        </li>
        <li>
          In the event of a member&apos;s resignation or expulsion, his or her
          name shall be removed from the Membership Register.
        </li>
      </ol>

      <h2>8. Club Committee</h2>
      <ol type="a">
        <li>
          <p>The Club Committee shall consist of:</p>
          <ol type="a" className="mt-2">
            <li>
              <p>Club Officers consisting of:</p>
              <ul className="mt-2">
                <li>Chairperson</li>
                <li>Vice Chairperson</li>
                <li>Club Secretary</li>
                <li>Treasurer</li>
                <li>Welfare Officer</li>
                <li>Disciplinary Secretary</li>
                <li>Minutes Secretary</li>
                <li>Fixtures Secretary</li>
                <li>Club Development and Communications Officer</li>
                <li>FA Accreditation Officer</li>
                <li>Registration Secretary</li>
                <li>Groundsperson</li>
              </ul>
            </li>
            <li className="mt-2">All Team Managers</li>
          </ol>
        </li>
        <li>
          The Club Committee shall be elected at an Annual General Meeting.
        </li>
        <li>
          Each Club Officer and Club Committee Member shall hold office from the
          date of appointment until the next Annual General Meeting
          (&ldquo;AGM&rdquo;) unless otherwise resolved at an Extraordinary
          General Meeting (&ldquo;EGM&rdquo;). One person may hold no more than
          two positions of Club Officer at any time. The Club Committee shall be
          responsible for the management of all the affairs of the Club.
          Decisions of the Club Committee shall be made by a simple majority of
          those attending the Club Committee meeting. The Chairperson of the Club
          Committee meeting shall have a casting vote in the event of a tie.
          Meetings of the Club Committee shall be chaired by the Chairperson or
          in their absence by either the Vice Chairperson or Club Secretary. The
          quorum for the transaction of business of the Club Committee shall be
          four.
        </li>
        <li>
          Decisions of Club Committee meetings shall be entered into the Minute
          Book of the Club.
        </li>
        <li>
          Any member of the Club Committee may call a meeting of the Club
          Committee by giving not less than seven days&apos; notice to all
          members of the Club Committee. The Club Committee shall hold not less
          than four meetings a year.
        </li>
        <li>
          An outgoing member of the Club Committee may be re-elected. Any vacancy
          on the Club Committee which arises between Annual General Meetings
          shall be filled by a member proposed by one and seconded by another of
          the remaining Club Committee members and approved by a simple majority
          of the remaining Club Committee members.
        </li>
        <li>
          The Club Committee shall have the power to appoint such sub-committees
          as may from time to time be deemed necessary and shall receive reports
          from such sub-committees at the Club Committee meetings.
        </li>
        <li>
          Save as provided for in the Rules and Regulations of The FA, the Parent
          County Association and any applicable Competition, the Club Committee
          shall have the power to decide all questions and disputes arising in
          any issue concerning the Club Rules.
        </li>
        <li>
          The position of a Club Officer shall be vacated if such person is
          subject to a decision of The FA that such person be suspended from
          holding office or from taking part in any football activity relating to
          the administration or management of a football club.
        </li>
        <li>
          All members of the Club Committee are expected to attend each Club
          Committee meeting. Where attendance is not possible, the Club Committee
          member must elect someone to represent them in their absence.
        </li>
        <li>
          The Club Committee shall have the power to declare vacant any Club
          Committee seat should that Club Committee member, without satisfactory
          explanation, be absent from three consecutive Club Committee meetings.
        </li>
      </ol>

      <h2>9. Annual and Extraordinary General Meetings</h2>
      <ol type="a">
        <li>
          <p>
            An AGM shall be held not later than 31st May each year to:
          </p>
          <ul className="mt-2">
            <li>
              receive a report of the activities of the Club over the previous
              year;
            </li>
            <li>
              receive a report of the Club&apos;s finances over the previous
              year;
            </li>
            <li>elect the members of the Club Committee; and</li>
            <li>consider any other business.</li>
          </ul>
        </li>
        <li>
          Nominations for election of members as Club Officers or as members of
          the Club Committee shall be proposed and seconded by existing members
          of the Club.
        </li>
        <li>
          Notice of any resolution to be proposed at the AGM shall be given in
          writing to the Club Secretary not less than 21 days before the meeting.
        </li>
        <li>
          An EGM may be called at any time by the Club Committee and shall be
          called within 21 days of the receipt by the Club Secretary of a
          requisition in writing, signed by not less than three members stating
          the purposes for which the Meeting is required and the resolutions
          proposed. Business at an EGM may be any business that may be transacted
          at an AGM.
        </li>
        <li>
          The Secretary shall issue notice of the date of a General Meeting
          (whether an AGM or an EGM) together with the resolutions to be
          proposed at least 14 days before the meeting.
        </li>
        <li>The quorum for a General Meeting shall be six.</li>
        <li>
          The Chairperson, or in their absence a member selected by the Club
          Committee, shall take the chair. Each member present shall have one
          vote and resolutions shall be passed by a two-thirds majority.
        </li>
        <li>
          Any member aged under 18 years may not vote in a general meeting save
          that one of his or her parents/guardians may vote on his or her behalf.
        </li>
        <li>
          Minutes of General Meetings shall be entered into the Minute Book of
          the Club.
        </li>
      </ol>

      <h2>10. Club Teams</h2>
      <p>
        At its AGM the Club Committee shall appoint a Club member to be
        responsible for each of the Club&apos;s football teams. The appointed
        members shall be responsible for managing the affairs of the team. The
        appointed members shall present to the Club Committee at its last meeting
        prior to an AGM a report of the activities of the team.
      </p>

      <h2>11. Club Finances</h2>
      <ol type="a">
        <li>
          A bank account shall be opened and maintained in the name of the Club
          (the &ldquo;Club Account&rdquo;). Designated account signatories shall
          be the Club Chairperson, the Club Secretary and the Treasurer. No sum
          shall be drawn from the Club Account except where authorised by two of
          the three designated signatories. All monies payable to the Club shall
          be received by the Treasurer and deposited in the Club Account.
        </li>
        <li>
          All surplus income or profits are to be reinvested in the Club. No
          surpluses or assets will be distributed to members or third-parties.
        </li>
        <li>
          The Club Committee shall have the power to authorise the payment of
          remuneration and expenses to any member of the Club (although a Club
          shall not remunerate a member for playing) and to any other person or
          persons for services rendered to the Club.
        </li>
        <li>
          No Team Manager shall incur a single item of expenditure greater than
          &pound;15 without the prior approval of a club officer. All
          expenditure must be supported by receipts.
        </li>
        <li>
          The Club may provide sporting and related social facilities, sporting
          equipment, coaching, courses, insurance cover, medical treatment,
          away-match expenses, post match refreshments and other ordinary
          benefits of Community Amateur Sports Clubs as provided for in the
          Finance Act 2002.
        </li>
        <li>
          <p>
            The Club may also in connection with the sports purposes of the Club:
          </p>
          <ul className="mt-2">
            <li>
              sell and supply food, drink and related sports clothing and
              equipment;
            </li>
            <li>
              employ members (although not for playing) and remunerate them for
              providing goods and services, on fair terms set by the Club
              Committee without the person concerned being present;
            </li>
            <li>
              pay for reasonable hospitality for visiting teams and guests; and
            </li>
            <li>
              indemnify the Club Committee and members acting properly in the
              course of the running of the Club against any liability incurred in
              the proper running of the Club (but only to the extent of its
              assets).
            </li>
          </ul>
        </li>
        <li>
          The Club shall keep accounting records for recording the fact and
          nature of all payments and receipts so as to disclose, with reasonable
          accuracy, at any time, the financial position, including the assets and
          liabilities of the Club. The Club must retain its accounting records
          for a minimum of six years.
        </li>
        <li>
          The Club shall prepare an annual &ldquo;Financial Statement&rdquo;, in
          such format as shall be available from The FA from time to time. The
          Financial Statement shall be audited and shall be approved by members
          at general meeting. A copy of any Financial Statement shall, on demand,
          be forwarded to The FA.
        </li>
        <li>
          The Club Property, other than the Club Account, shall be vested in The
          Club Committee (&ldquo;the Custodians&rdquo;).
        </li>
        <li>
          Upon removal, resignation or death of a Custodian any Club Property
          vested in them shall vest automatically in the surviving Custodians.
        </li>
        <li>
          The Custodians shall be entitled to an indemnity out of the Club
          Property for all expenses and other liabilities reasonably incurred by
          them in carrying out their duties.
        </li>
      </ol>

      <h2>12. Fines</h2>
      <ol type="a">
        <li>
          All fines (including any additional administration fees) incurred by
          members, including club officials, whilst representing the club are to
          be paid by that member not later than the due-date and not the club.
        </li>
        <li>
          Where fines have initially been settled by the club, then the member
          who incurred said fines must reimburse the club within 28 days.
        </li>
        <li>
          The Club Committee shall have the power to expel any member in the
          event of non-payment of fines by that member. An appeal against such a
          decision may be made to the Club Committee in accordance with the
          Complaints Procedure in force from time to time.
        </li>
      </ol>

      <h2>13. Dissolution</h2>
      <ol type="a">
        <li>
          A resolution to dissolve the Club shall only be proposed at a General
          Meeting and shall be carried by a majority of at least three-quarters
          of the members present.
        </li>
        <li>
          The dissolution shall take effect from the date of the resolution and
          the members of the Club Committee shall be responsible for the winding
          up of the assets and liabilities of the Club.
        </li>
        <li>
          Upon dissolution of the Club, any remaining assets remaining after the
          discharge of the debts and liabilities of the Club shall be given or
          transferred to another registered CASC, a registered charity, the
          Parent County Association or The FA for use by them in related
          community sports.
        </li>
      </ol>

      <h2>14. Pitch Marking</h2>
      <p>
        Team Managers are responsible for pitch marking upon the dates assigned
        to them in the pitch marking rotas, as may be agreed at club meetings
        and recorded in the Club minutes.
      </p>

      <h2>15. First Aid</h2>
      <p>
        At the time of application for membership, all parents/guardians of the
        applicant are to be made aware that the club requires their authority to
        apply first aid and treatment to a member in the event of injury.
      </p>
    </PolicyPage>
  );
}
